function openCard1(){
    document.getElementById("card_body1").style.height="auto";
    document.getElementById("card_body1").style.opacity=1;
    document.getElementById("down1").style.visibility="hidden";
    document.getElementById("up1").style.visibility="visible";
}
function closeCard1(){
    document.getElementById("card_body1").style.height=0;
    document.getElementById("card_body1").style.opacity=0;
    document.getElementById("down1").style.visibility="visible";
    document.getElementById("up1").style.visibility="hidden";

}
